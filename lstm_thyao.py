features = ["Open", "High", "Low", "Close", "Volume"]

model_data = data[["Date"] + features].copy()

model_data.head()

dataset = model_data[features].values

print(dataset.shape)

from sklearn.preprocessing import MinMaxScaler

scaler = MinMaxScaler(feature_range=(0, 1))
scaled_data = scaler.fit_transform(dataset)

print(scaled_data[:5])
print(scaled_data.shape)

train_size = int(len(scaled_data) * 0.8)

train_data = scaled_data[:train_size]
test_data = scaled_data[train_size:]

print("Eğitim veri sayısı:", train_data.shape)
print("Test veri sayısı:", test_data.shape)

import numpy as np

def create_multivariate_dataset(dataset, time_step=60, target_col=3):
    X, y = [], []
    for i in range(time_step, len(dataset)):
        X.append(dataset[i-time_step:i, :])
        y.append(dataset[i, target_col])
    return np.array(X), np.array(y)

time_step = 60

X_train, y_train = create_multivariate_dataset(train_data, time_step, target_col=3)
X_test, y_test = create_multivariate_dataset(test_data, time_step, target_col=3)

print("X_train:", X_train.shape)
print("y_train:", y_train.shape)
print("X_test:", X_test.shape)
print("y_test:", y_test.shape)

print("Bir örnek X boyutu:", X_train[0].shape)
print("Bir örnek y değeri:", y_train[0])

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout

model = Sequential()

model.add(LSTM(units=64, return_sequences=True, input_shape=(X_train.shape[1], X_train.shape[2])))
model.add(Dropout(0.2))

model.add(LSTM(units=32, return_sequences=False))
model.add(Dropout(0.2))

model.add(Dense(units=16))
model.add(Dense(units=1))

model.compile(optimizer="adam", loss="mean_squared_error")

model.summary()

history = model.fit(
    X_train, y_train,
    epochs=50,
    batch_size=32,
    validation_data=(X_test, y_test),
    verbose=1
)

import matplotlib.pyplot as plt

plt.figure(figsize=(10,5))
