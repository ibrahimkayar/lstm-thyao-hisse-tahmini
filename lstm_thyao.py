def inverse_close_transform(scaled_close_values, scaler, close_index=3, n_features=5):
    dummy = np.zeros((len(scaled_close_values), n_features))
    dummy[:, close_index] = scaled_close_values.reshape(-1)
    inversed = scaler.inverse_transform(dummy)
    return inversed[:, close_index]

predicted_close = inverse_close_transform(predictions, scaler)
actual_close = inverse_close_transform(y_test, scaler)

print(predicted_close[:5])
print(actual_close[:5])

from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
import numpy as np

